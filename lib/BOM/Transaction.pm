sub _build_contract {
    my $self  = shift;
    my $param = $self->contract_parameters;

    if ($param->{shortcode}) {
        $param = shortcode_to_parameters($param->{shortcode}, $param->{currency});
        $param->{landing_company} = $self->contract_parameters->{landing_company};
    }
    return produce_contract($param);
}

